#Создаём переменную для хранения  строки "нейропрограммирование".
string = "нейропластичность"

# Извлекаем строку без первого и последнего символа
substring = string[1:-1]

# Разворачиваем полученную строку
reversed_substring = substring[::-1]

# Выводим результат  «тсончитсалпорйе» на экран
print(reversed_substring)